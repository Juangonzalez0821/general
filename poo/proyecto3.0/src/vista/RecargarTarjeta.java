    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        Bloqueos bloqueos = new Bloqueos();
        bloqueos.setIdTarjeta(jTextField1.getText());
        
        TarjetaRecargar reload = new TarjetaRecargar();

        int estadoTarjeta = bloqueos.consultaEstado();
        if (estadoTarjeta == 1) {
            JOptionPane.showMessageDialog(null, "La tarjeta est√° bloqueada.");
        } else {           

            reload.setIdTarjeta(jTextField1.getText());
            reload.setValorARecargar(jTextField2.getText());
            reload.recargar();
            JOptionPane.showMessageDialog(null, "Recarga Exitosa");
            ValidarPasaje consulta = new ValidarPasaje();
            consulta.setIdTarjeta(jTextField1.getText());

            String saldo = consulta.consultarSaldo();
            jLabel6.setText(saldo);
            System.out.println(saldo);
        }
    }// GEN-LAST:event_jButton1ActionPerformed

